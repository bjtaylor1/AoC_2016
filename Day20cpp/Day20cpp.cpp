long get_first_zero(const long long l)
{
	for (int i = 0; i < 64; i++)
	{
		if (((l >> i) & 1) == 0)
		{
			return i;
		}
	}
}

long get_count_zero(const long long l)
{
	long answer = 0;
	for (int i = 0; i < 64; i++)
	{
		if (((l >> i) & 1) == 0)
		{
			answer++;
		}
	}
	return answer;
}

long long get_first_unblocked(const long long* l)
{
	for (long long i = 0; i < NUM_LONGS; i++)
	{
		if (l[i] != full)
		{
			long long answer = i * 64 + get_first_zero(l[i]);
			return answer;
		}
	}
}

long long get_count_unblocked(const long long* l)
{
	long long result = 0;
	for (long long i = 0; i < NUM_LONGS; i++)
	{
		if (l[i] != full) result += get_count_zero(l[i]);
	}
	return result;
}

