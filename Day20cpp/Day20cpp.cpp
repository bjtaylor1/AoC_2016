using namespace std;
void setbit(long long* l, long long bit)
{
	long long index = bit / 64;
	long long r = bit % 64;
	//cout << "setting " << index << " , " << r << endl;
	long long op = ((long long)1 << r);
	l[index] |= op;
}
const long long full = (long long)-1;

void blacklist(long long* l, long long start, long long end)
{
	cout << start << " - " << end << endl;
	long long minindex = (start / 64) + 1;
	long long maxindex = end / 64;
	if (maxindex > minindex)
	{
		long long totallyfull = maxindex - minindex;
		memset(l + minindex, full, totallyfull * sizeof(long long));
	}
	for (long long i = start; i < minindex * 64 && i <= end; i++)
	{
		setbit(l, i);
	}
	if (maxindex > minindex)
	{
		for (long long i = maxindex * 64; i <= end; i++)
		{
			setbit(l, i);
		}
	}
}

